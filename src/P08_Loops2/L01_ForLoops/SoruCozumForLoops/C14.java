package P08_Loops2.L01_ForLoops.SoruCozumForLoops;

public class C14 {
    public static void main(String[] args) {
        for (int i = 0; i <10 ; i++) {
            for (int j = 0; j <3 ; j++) {
                System.out.println(i+" "+j);
            }
            System.out.println();
        }
    }
}
